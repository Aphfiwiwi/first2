fun main() {
    //arithmetic operators
    var x = 20
    var y = 5

    println( x + y)
    println( x * y)
    println( x / y)
    println( x - y)
    println( x % y) //modulus

    //comparison operators
    var f =69
    var e =13

    println(f > e)
    println(f < e)
    println(f == e)
    println(f != e)

    //assignment operators
    var q = 80

    println(q)

    q +=3
    println(q)

    q -=3
    println(q)

    q *=3
    println(q)

    //logical operators(or-||, not-! and-&&)
    var t = 80
    println(t > 79 &&  t < 99)
    println(!(t > 79 &&  t < 99))
    println(t > 79 ||  t < 99)




}